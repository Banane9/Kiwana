        /// <summary>
        /// Accepts the NewLine event of the <see cref="Kiwana"/> class.
        /// </summary>
        /// <param name="ex">The line from the server, split at spaces.</param>
        /// <param name="recipient">The channel or user the message came from. Empty when from console.</param>
        /// <param name="command">The normalized command. Empty if there's no command.</param>
        /// <param name="userAuthorized">Whether the user sending the command is authorized. False if there's no command.</param>
        /// <param name="console">Whether the line came from the console.</param>
        public override void HandleLine(List<string> ex, string recipient, string command, bool userAuthorized, bool console)
        {
            if (userAuthorized)
            {
                switch (command)
                {
                    case "doge":
                        _sendArt(recipient, _doge);
                        break;
                    case "lol":
                        _sendArt(recipient, _lol);
                        break;
                    case "troll":
                        _sendArt(recipient, _troll);
                        break;
                    case "trollgirl":
                        _sendArt(recipient, _trollGirl);
                        break;
                    case "bitchplease":
                        _sendArt(recipient, _bitchPlease);
                        break;
                    case "megusta":
                        _sendArt(recipient, _meGusta);
                        break;
                    case "notbad":
                        _sendArt(recipient, _notBad);
                        break;
                    case "youdon'tsay":
                        _sendArt(recipient, _youDontSay);
                        break;
                    case "areyoufuckingkiddingme":
                        _sendArt(recipient, _areYouFuckingKiddingMe);
                        break;
                    case "newspaperguy":
                        _sendArt(recipient, _newspaperGuy);
                        break;
                    case "ohgodwhy":
                        _sendArt(recipient, _ohGodWhy);
                        break;
                    case "iknowthatfeelbro":
                        _sendArt(recipient, _iKnowThatFeelBro);
                        break;
                    case "nothingtodohere":
                        _sendArt(recipient, _nothingToDoHere);
                        break;
                    case "nyancat":
                        _sendArt(recipient, _nyanCat);
                        break;
                    case "objection":
                        _sendArt(recipient, _objection);
                        break;
                }
            }

        }

        /// <summary>
        /// Iterates through the array containing the ASCII-Art piece.
        /// </summary>
        /// <param name="recipient">The receiver of the messages.</param>
        /// <param name="art">The array of lines for the image.</param>
        private void _sendArt(string recipient, string[] art)
        {
            foreach (string line in art)
            {
                SendData(MessageTypes.PRIVMSG, recipient + " :" + line);
            }
        }

